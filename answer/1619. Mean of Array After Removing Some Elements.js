/**
 * @param {number[]} arr
 * @return {number}
 */
var trimMean = function (arr) {
    const count = arr.length * 0.05
    let minArr = arr.slice(0, count), maxArr = arr.slice(0, count)
    minArr.sort((a, b) => a - b)
    maxArr.sort((a, b) => b - a)
    return ((arr.reduce((rs, num, idx) => {
        if (idx < count) return rs + num
        const minIdx = minArr.findIndex(i => num < i)
        minIdx !== -1 && minArr.splice(minIdx, 0, num) && minArr.pop()
        const maxIdx = maxArr.findIndex(i => num > i)
        maxIdx !== -1 && maxArr.splice(maxIdx, 0, num) && maxArr.pop()
        return rs + num
    }, 0) - [...minArr, ...maxArr].reduce((rs, i) => rs + i)) / (arr.length - 2 * count)).toFixed(5)
};
